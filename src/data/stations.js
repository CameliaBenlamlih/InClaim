export function searchStations(query) {
  if (!query || query.length < 2) return [];
  
  const searchTerm = query.toLowerCase();
  
  return STATIONS.filter(station => 
    station.city.toLowerCase().includes(searchTerm) ||
    station.code.toLowerCase().includes(searchTerm) ||
    station.name.toLowerCase().includes(searchTerm) ||
    station.country.toLowerCase().includes(searchTerm)
  ).slice(0, 8);
}

export function formatStation(station) {
  return `${station.city} - ${station.name}`;
}
